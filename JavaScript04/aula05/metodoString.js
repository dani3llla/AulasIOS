let nome = 'Daniela Alves'

console.log(nome[0]);
console.log(nome.charAt(6));

console.log(nome.length); // retorna a quantidade de caracteres presentes na string

console.log(nome.charAt(12)); // CharAt acessa determinado caractere baseado no índice escolhido

console.log(nome.toLowerCase()); //Transformar todos os caracteres em minúsculo

console.log(nome.toUpperCase()); //Transforma todos os caracteres em maiúsculo

console.log(nome.substring(0, 7)); // Corta a string a partir do primeiro índice até o segundo índice

console.log(nome.split(' ')); // Split separa a string de acordo com o que você especificou

